#include<bits/stdc++.h>
using namespace std;
int main()
{
    int n;
    cin>>n;
    int a[n],sum=0;
    for(int i=0;i<n;i++)
    {
        cin>>a[i];
        sum+=a[i];
    }
    sort(a,a+n);
    int s=0,c=0;
    for(int i=n-1;i>=0;i--)
    {
        s=s+a[i];
        sum=sum-a[i];
        c++;
        if(s>sum)
            break;
    }
    cout<<c<<"\n";
}
 
